interface WorkerMessage {
  type: 'compute';
  scalarData: Float32Array;
  gridSize: { x: number; y: number; z: number };
  bounds: { min: { x: number; y: number; z: number }; max: { x: number; y: number; z: number } };
  threshold: number;
}

interface ResultMessage {
  type: 'result' | 'error';
  vertices?: Float32Array;
  normals?: Float32Array;
  threshold: number;
  message?: string;
}

function computeIsosurface(
  scalarData: Float32Array,
  gridSize: { x: number; y: number; z: number },
  bounds: { min: { x: number; y: number; z: number }; max: { x: number; y: number; z: number } },
  threshold: number
): { vertices: Float32Array; normals: Float32Array } {
  const { x: nx, y: ny, z: nz } = gridSize;
  const { min, max } = bounds;
  
  const dx = (max.x - min.x) / (nx - 1);
  const dy = (max.y - min.y) / (ny - 1);
  const dz = (max.z - min.z) / (nz - 1);

  const vertices: number[] = [];
  const normals: number[] = [];

  const edgeVerts: number[] = new Array(12 * 3);

  function getValue(i: number, j: number, k: number): number {
    if (i < 0 || i >= nx || j < 0 || j >= ny || k < 0 || k >= nz) return 0;
    return scalarData[(i * ny + j) * nz + k]!;
  }

  function getGradient(i: number, j: number, k: number): { x: number; y: number; z: number } {
    const h = 1;
    const dx_val = (getValue(i + h, j, k) - getValue(i - h, j, k)) / (2 * dx);
    const dy_val = (getValue(i, j + h, k) - getValue(i, j - h, k)) / (2 * dy);
    const dz_val = (getValue(i, j, k + h) - getValue(i, j, k - h)) / (2 * dz);
    return { x: -dx_val, y: -dy_val, z: -dz_val };
  }

  function interpolate(p1: { x: number; y: number; z: number }, p2: { x: number; y: number; z: number },
                      v1: number, v2: number, t: number): { x: number; y: number; z: number } {
    return {
      x: p1.x + (p2.x - p1.x) * t,
      y: p1.y + (p2.y - p1.y) * t,
      z: p1.z + (p2.z - p1.z) * t
    };
  }

  function normalize(v: { x: number; y: number; z: number }): { x: number; y: number; z: number } {
    const len = Math.sqrt(v.x * v.x + v.y * v.y + v.z * v.z) || 1;
    return { x: v.x / len, y: v.y / len, z: v.z / len };
  }

  for (let i = 0; i < nx - 1; i++) {
    for (let j = 0; j < ny - 1; j++) {
      for (let k = 0; k < nz - 1; k++) {
        const v0 = getValue(i, j, k);
        const v1 = getValue(i + 1, j, k);
        const v2 = getValue(i + 1, j + 1, k);
        const v3 = getValue(i, j + 1, k);
        const v4 = getValue(i, j, k + 1);
        const v5 = getValue(i + 1, j, k + 1);
        const v6 = getValue(i + 1, j + 1, k + 1);
        const v7 = getValue(i, j + 1, k + 1);

        let cubeIndex = 0;
        if (v0 < threshold) cubeIndex |= 1;
        if (v1 < threshold) cubeIndex |= 2;
        if (v2 < threshold) cubeIndex |= 4;
        if (v3 < threshold) cubeIndex |= 8;
        if (v4 < threshold) cubeIndex |= 16;
        if (v5 < threshold) cubeIndex |= 32;
        if (v6 < threshold) cubeIndex |= 64;
        if (v7 < threshold) cubeIndex |= 128;

        const edgeFlags = EDGE_TABLE[cubeIndex]!;
        if (edgeFlags === 0) continue;

        const x0 = min.x + i * dx;
        const y0 = min.y + j * dy;
        const z0 = min.z + k * dz;

        const p0 = { x: x0, y: y0, z: z0 };
        const p1 = { x: x0 + dx, y: y0, z: z0 };
        const p2 = { x: x0 + dx, y: y0 + dy, z: z0 };
        const p3 = { x: x0, y: y0 + dy, z: z0 };
        const p4 = { x: x0, y: y0, z: z0 + dz };
        const p5 = { x: x0 + dx, y: y0, z: z0 + dz };
        const p6 = { x: x0 + dx, y: y0 + dy, z: z0 + dz };
        const p7 = { x: x0, y: y0 + dy, z: z0 + dz };

        if (edgeFlags & 1) {
          const t = (threshold - v0) / (v1 - v0 || 1);
          const p = interpolate(p0, p1, v0, v1, t);
          edgeVerts[0] = p.x; edgeVerts[1] = p.y; edgeVerts[2] = p.z;
        }
        if (edgeFlags & 2) {
          const t = (threshold - v1) / (v2 - v1 || 1);
          const p = interpolate(p1, p2, v1, v2, t);
          edgeVerts[3] = p.x; edgeVerts[4] = p.y; edgeVerts[5] = p.z;
        }
        if (edgeFlags & 4) {
          const t = (threshold - v2) / (v3 - v2 || 1);
          const p = interpolate(p2, p3, v2, v3, t);
          edgeVerts[6] = p.x; edgeVerts[7] = p.y; edgeVerts[8] = p.z;
        }
        if (edgeFlags & 8) {
          const t = (threshold - v3) / (v0 - v3 || 1);
          const p = interpolate(p3, p0, v3, v0, t);
          edgeVerts[9] = p.x; edgeVerts[10] = p.y; edgeVerts[11] = p.z;
        }
        if (edgeFlags & 16) {
          const t = (threshold - v4) / (v5 - v4 || 1);
          const p = interpolate(p4, p5, v4, v5, t);
          edgeVerts[12] = p.x; edgeVerts[13] = p.y; edgeVerts[14] = p.z;
        }
        if (edgeFlags & 32) {
          const t = (threshold - v5) / (v6 - v5 || 1);
          const p = interpolate(p5, p6, v5, v6, t);
          edgeVerts[15] = p.x; edgeVerts[16] = p.y; edgeVerts[17] = p.z;
        }
        if (edgeFlags & 64) {
          const t = (threshold - v6) / (v7 - v6 || 1);
          const p = interpolate(p6, p7, v6, v7, t);
          edgeVerts[18] = p.x; edgeVerts[19] = p.y; edgeVerts[20] = p.z;
        }
        if (edgeFlags & 128) {
          const t = (threshold - v7) / (v4 - v7 || 1);
          const p = interpolate(p7, p4, v7, v4, t);
          edgeVerts[21] = p.x; edgeVerts[22] = p.y; edgeVerts[23] = p.z;
        }
        if (edgeFlags & 256) {
          const t = (threshold - v0) / (v4 - v0 || 1);
          const p = interpolate(p0, p4, v0, v4, t);
          edgeVerts[24] = p.x; edgeVerts[25] = p.y; edgeVerts[26] = p.z;
        }
        if (edgeFlags & 512) {
          const t = (threshold - v1) / (v5 - v1 || 1);
          const p = interpolate(p1, p5, v1, v5, t);
          edgeVerts[27] = p.x; edgeVerts[28] = p.y; edgeVerts[29] = p.z;
        }
        if (edgeFlags & 1024) {
          const t = (threshold - v2) / (v6 - v2 || 1);
          const p = interpolate(p2, p6, v2, v6, t);
          edgeVerts[30] = p.x; edgeVerts[31] = p.y; edgeVerts[32] = p.z;
        }
        if (edgeFlags & 2048) {
          const t = (threshold - v3) / (v7 - v3 || 1);
          const p = interpolate(p3, p7, v3, v7, t);
          edgeVerts[33] = p.x; edgeVerts[34] = p.y; edgeVerts[35] = p.z;
        }

        let triIndex = 0;
        while (TRI_TABLE[cubeIndex * 16 + triIndex] !== -1) {
          for (let t = 0; t < 3; t++) {
            const edgeIdx = TRI_TABLE[cubeIndex * 16 + triIndex + t]!;
            const vx = edgeVerts[edgeIdx * 3]!;
            const vy = edgeVerts[edgeIdx * 3 + 1]!;
            const vz = edgeVerts[edgeIdx * 3 + 2]!;
            vertices.push(vx, vy, vz);
            
            const ni = i, nj = j, nk = k;
            const grad = normalize(getGradient(ni, nj, nk));
            normals.push(grad.x, grad.y, grad.z);
          }
          triIndex += 3;
        }
      }
    }
  }

  return {
    vertices: new Float32Array(vertices),
    normals: new Float32Array(normals)
  };
}

self.onmessage = function(e: MessageEvent<WorkerMessage>) {
  try {
    const { scalarData, gridSize, bounds, threshold } = e.data;
    const result = computeIsosurface(scalarData, gridSize, bounds, threshold);
    
    const message: ResultMessage = {
      type: 'result',
      vertices: result.vertices,
      normals: result.normals,
      threshold
    };
    
    self.postMessage(message, [result.vertices.buffer, result.normals.buffer]);
  } catch (error) {
    const err = error as Error;
    self.postMessage({
      type: 'error',
      threshold: e.data.threshold,
      message: err.message
    } as ResultMessage);
  }
};
