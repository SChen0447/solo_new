function createFrame(jointPositions: Array<[number, number]>): { joints: Array<{ name: string; x: number; y: number; parent: string | null }> } {
  const parents: (string | null)[] = [
    'neck',
    'hips',
    'neck',
    'neck',
    'leftShoulder',
    'rightShoulder',
    'leftElbow',
    'rightElbow',
    null,
    'hips',
    'hips',
    'leftKnee',
    'rightKnee'
  ];

  return {
    joints: jointPositions.map(([x, y], i) => ({
      name: JOINT_NAMES[i],
      x,
      y,
      parent: parents[i]
    }))
  };
}

