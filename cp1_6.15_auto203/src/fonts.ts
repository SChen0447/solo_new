export function calculateCompatibilityScore(
  fontTitle: FontItem,
  fontBody: FontItem
): number {
  const xHeightDiff = Math.abs(fontTitle.xHeight - fontBody.xHeight);
  const widthRatioDiff = Math.abs(fontTitle.widthRatio - fontBody.widthRatio);
  const kerningDiff = Math.abs(fontTitle.avgKerning - fontBody.avgKerning);

  const xHeightPenalty = xHeightDiff * 0.4;
  const widthRatioPenalty = widthRatioDiff * 0.3;
  const kerningPenalty = kerningDiff * 10 * 0.3;

  const totalPenalty = xHeightPenalty + widthRatioPenalty + kerningPenalty;
  const score = Math.max(0, Math.min(100, 100 - totalPenalty * 100));

  return Math.round(score);
}

export function getCompatibilityAdvice(
  fontTitle: FontItem,
  fontBody: FontItem
): string {
  const xHeightDiff = Math.abs(fontTitle.xHeight - fontBody.xHeight);
  const widthRatioDiff = Math.abs(fontTitle.widthRatio - fontBody.widthRatio);
  const kerningDiff = Math.abs(fontTitle.avgKerning - fontBody.avgKerning);

  const advices: string[] = [];

  if (xHeightDiff > 0.08) {
    advices.push('x-height 差异较大，建议换用 x-height 更接近的字体组合');
  }
  if (widthRatioDiff > 0.08) {
    advices.push('字面率差异明显，可能影响视觉和谐度');
  }
  if (kerningDiff > 0.015) {
    advices.push('字偶间距差异较大，可考虑调整字间距参数');
  }

  if (fontTitle.type === fontBody.type) {
    advices.push('标题与正文使用同类型字体，可考虑混搭不同字体类型增强层次');
  }

  if (advices.length === 0) {
    advices.push('这组字体搭配协调，视觉效果良好！');
  }

  return advices[0];
}

export function getScoreColor(score: number): string {
  if (score >= 85) return '#4caf50';
  if (score >= 70) return '#ff9800';
  return '#f44336';
}

export function getScoreLabel(score: number): string {
  if (score >= 90) return '非常协调';
  if (score >= 80) return '比较协调';
  if (score >= 70) return '一般';
  if (score >= 60) return '不太协调';
  return '差异较大';
}
